import { mkdirSync, writeFileSync } from "node:fs";
import path from "node:path";
import process from "node:process";

const targetValues = new Set(["staging", "production"]);
const engineValues = new Set(["postgresql"]);
const identifierPattern = /^[a-z_][a-z0-9_]*$/;

const readText = (name, fallback = "") => (process.env[name] || fallback).trim();

const requireText = (name) => {
  const value = readText(name);

  if (!value) {
    throw new Error(`${name} is required.`);
  }

  return value;
};

const optionalText = (name) => {
  const value = readText(name);
  return value || null;
};

const sanitizeLabel = (value) => value.replace(/[^A-Za-z0-9._-]/g, "-").replace(/-+/g, "-");

const requireTarget = () => {
  const target = requireText("API_DATABASE_SCHEMA_TARGET");

  if (!targetValues.has(target)) {
    throw new Error("API_DATABASE_SCHEMA_TARGET must be staging or production.");
  }

  return target;
};

const requireEngine = () => {
  const engine = requireText("API_DATABASE_SCHEMA_ENGINE");

  if (!engineValues.has(engine)) {
    throw new Error("API_DATABASE_SCHEMA_ENGINE must be postgresql.");
  }

  return engine;
};

const requireIdentifier = (name, fallback) => {
  const value = readText(name, fallback);

  if (!identifierPattern.test(value)) {
    throw new Error(`${name} must be a lowercase PostgreSQL identifier.`);
  }

  return value;
};

const quoteIdentifier = (value) => `"${value.replaceAll('"', '""')}"`;

const buildTableName = (schemaName, tableName) => `${quoteIdentifier(schemaName)}.${quoteIdentifier(tableName)}`;

const buildPostgresqlSchema = (schemaName) => {
  const vaultsTable = buildTableName(schemaName, "vaults");
  const eventsTable = buildTableName(schemaName, "vault_events");
  const syncStatesTable = buildTableName(schemaName, "sync_states");
  const analyticsEventsTable = buildTableName(schemaName, "analytics_events");
  const supportRequestsTable = buildTableName(schemaName, "support_requests");

  return [
    "BEGIN;",
    `CREATE SCHEMA IF NOT EXISTS ${quoteIdentifier(schemaName)};`,
    `CREATE TABLE IF NOT EXISTS ${vaultsTable} (`,
    "  key TEXT PRIMARY KEY,",
    "  chain_id INTEGER NOT NULL,",
    "  contract_address TEXT NOT NULL,",
    "  owner_wallet TEXT,",
    "  asset_address TEXT,",
    "  target_amount_atomic TEXT,",
    "  rule_type TEXT NOT NULL,",
    "  unlock_date TEXT,",
    "  cooldown_duration_seconds INTEGER,",
    "  guardian_address TEXT,",
    "  unlock_requested_at TEXT,",
    "  unlock_eligible_at TEXT,",
    "  unlock_request_status TEXT NOT NULL,",
    "  guardian_approval_state TEXT NOT NULL,",
    "  guardian_decision_at TEXT,",
    "  created_at TEXT,",
    "  created_tx_hash TEXT,",
    "  display_name TEXT,",
    "  category TEXT,",
    "  note TEXT,",
    "  accent_theme TEXT,",
    "  metadata_status TEXT NOT NULL,",
    "  reconciliation_status TEXT NOT NULL,",
    "  total_deposited_atomic TEXT NOT NULL,",
    "  total_withdrawn_atomic TEXT NOT NULL,",
    "  current_balance_atomic TEXT NOT NULL,",
    "  last_activity_at TEXT,",
    "  last_indexed_at TEXT,",
    "  onchain_found BOOLEAN NOT NULL DEFAULT FALSE",
    ");",
    `CREATE UNIQUE INDEX IF NOT EXISTS vaults_chain_contract_idx ON ${vaultsTable} (chain_id, lower(contract_address));`,
    `CREATE INDEX IF NOT EXISTS vaults_owner_idx ON ${vaultsTable} (chain_id, lower(owner_wallet)) WHERE owner_wallet IS NOT NULL;`,
    `CREATE TABLE IF NOT EXISTS ${eventsTable} (`,
    "  id TEXT PRIMARY KEY,",
    "  chain_id INTEGER NOT NULL,",
    "  tx_hash TEXT NOT NULL,",
    "  block_number BIGINT NOT NULL,",
    "  log_index INTEGER NOT NULL,",
    "  vault_address TEXT NOT NULL,",
    "  owner_address TEXT,",
    "  actor_address TEXT,",
    "  event_type TEXT NOT NULL,",
    "  amount_atomic TEXT,",
    "  occurred_at TEXT NOT NULL,",
    "  indexed_at TEXT NOT NULL",
    ");",
    `CREATE INDEX IF NOT EXISTS vault_events_chain_vault_idx ON ${eventsTable} (chain_id, lower(vault_address), block_number, log_index);`,
    `CREATE INDEX IF NOT EXISTS vault_events_chain_owner_idx ON ${eventsTable} (chain_id, lower(owner_address), block_number, log_index) WHERE owner_address IS NOT NULL;`,
    `CREATE TABLE IF NOT EXISTS ${syncStatesTable} (`,
    "  key TEXT PRIMARY KEY,",
    "  chain_id INTEGER NOT NULL,",
    "  stream_type TEXT NOT NULL,",
    "  scope_key TEXT NOT NULL,",
    "  lifecycle TEXT NOT NULL,",
    "  latest_indexed_block BIGINT,",
    "  latest_indexed_log_index INTEGER,",
    "  latest_chain_block BIGINT,",
    "  last_synced_at TEXT,",
    "  error_message TEXT",
    ");",
    `CREATE INDEX IF NOT EXISTS sync_states_chain_stream_idx ON ${syncStatesTable} (chain_id, stream_type);`,
    `CREATE TABLE IF NOT EXISTS ${analyticsEventsTable} (`,
    "  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,",
    "  name TEXT NOT NULL,",
    "  category TEXT NOT NULL,",
    "  occurred_at TEXT NOT NULL,",
    "  platform TEXT NOT NULL,",
    "  route TEXT NOT NULL,",
    "  environment TEXT NOT NULL,",
    "  deployment_target TEXT NOT NULL,",
    "  chain_id INTEGER,",
    "  wallet_status TEXT,",
    "  sync_freshness TEXT,",
    "  vault_address TEXT,",
    "  tx_hash TEXT,",
    "  context_json TEXT NOT NULL,",
    "  payload_json TEXT NOT NULL",
    ");",
    `CREATE INDEX IF NOT EXISTS analytics_events_name_idx ON ${analyticsEventsTable} (name, occurred_at);`,
    `CREATE INDEX IF NOT EXISTS analytics_events_route_idx ON ${analyticsEventsTable} (route, occurred_at);`,
    `CREATE INDEX IF NOT EXISTS analytics_events_vault_idx ON ${analyticsEventsTable} (lower(vault_address), occurred_at) WHERE vault_address IS NOT NULL;`,
    `CREATE TABLE IF NOT EXISTS ${supportRequestsTable} (`,
    "  id TEXT PRIMARY KEY,",
    "  status TEXT NOT NULL,",
    "  category TEXT NOT NULL,",
    "  priority TEXT NOT NULL,",
    "  subject TEXT NOT NULL,",
    "  message TEXT NOT NULL,",
    "  reporter_wallet TEXT,",
    "  contact_email TEXT,",
    "  route TEXT,",
    "  environment TEXT NOT NULL,",
    "  deployment_target TEXT NOT NULL,",
    "  chain_id INTEGER,",
    "  wallet_status TEXT,",
    "  vault_address TEXT,",
    "  user_agent TEXT,",
    "  requester_ip_hash TEXT,",
    "  created_at TEXT NOT NULL",
    ");",
    `CREATE INDEX IF NOT EXISTS support_requests_status_created_idx ON ${supportRequestsTable} (status, created_at);`,
    `CREATE INDEX IF NOT EXISTS support_requests_priority_created_idx ON ${supportRequestsTable} (priority, created_at);`,
    `CREATE INDEX IF NOT EXISTS support_requests_wallet_idx ON ${supportRequestsTable} (lower(reporter_wallet), created_at) WHERE reporter_wallet IS NOT NULL;`,
    `CREATE INDEX IF NOT EXISTS support_requests_vault_idx ON ${supportRequestsTable} (lower(vault_address), created_at) WHERE vault_address IS NOT NULL;`,
    "COMMIT;",
    "",
  ].join("\n");
};

const target = requireTarget();
const engine = requireEngine();
const schemaName = requireIdentifier("API_DATABASE_SCHEMA_NAME", "goal_vault_api");
const schemaLabel = sanitizeLabel(requireText("API_DATABASE_SCHEMA_LABEL"));
const outputDir = readText("API_DATABASE_SCHEMA_DIR", path.join(process.cwd(), "artifacts"));
const schemaSql = buildPostgresqlSchema(schemaName);

const manifest = {
  app: "pocket-vault",
  component: "api-managed-database-schema",
  target,
  engine,
  schemaName,
  schemaLabel,
  generatedAt: new Date().toISOString(),
  noDatabaseMutated: true,
  currentPersistence: "sqlite",
  sourcePlan: optionalText("API_DATABASE_SCHEMA_SOURCE_PLAN"),
  tables: [
    {
      name: "vaults",
      sourceFile: "goal-vault-indexer.sqlite",
      primaryKey: ["key"],
      indexes: ["vaults_chain_contract_idx", "vaults_owner_idx"],
      containsPrivateMetadata: true,
    },
    {
      name: "vault_events",
      sourceFile: "goal-vault-indexer.sqlite",
      primaryKey: ["id"],
      indexes: ["vault_events_chain_vault_idx", "vault_events_chain_owner_idx"],
      containsPrivateMetadata: false,
    },
    {
      name: "sync_states",
      sourceFile: "goal-vault-indexer.sqlite",
      primaryKey: ["key"],
      indexes: ["sync_states_chain_stream_idx"],
      containsPrivateMetadata: false,
    },
    {
      name: "analytics_events",
      sourceFile: "goal-vault-analytics.sqlite",
      primaryKey: ["id"],
      indexes: ["analytics_events_name_idx", "analytics_events_route_idx", "analytics_events_vault_idx"],
      containsPrivateMetadata: false,
    },
    {
      name: "support_requests",
      sourceFile: "goal-vault-analytics.sqlite",
      primaryKey: ["id"],
      indexes: [
        "support_requests_status_created_idx",
        "support_requests_priority_created_idx",
        "support_requests_wallet_idx",
        "support_requests_vault_idx",
      ],
      containsPrivateMetadata: true,
    },
  ],
  git: {
    commitSha: optionalText("GITHUB_SHA"),
    refName: optionalText("GITHUB_REF_NAME"),
    workflowRunId: optionalText("GITHUB_RUN_ID"),
  },
};

mkdirSync(outputDir, { recursive: true });

const baseName = `pocket-vault-api-database-schema-${target}-${schemaLabel}`;
const schemaPath = path.join(outputDir, `${baseName}.sql`);
const manifestPath = path.join(outputDir, `${baseName}.json`);

writeFileSync(schemaPath, schemaSql);
writeFileSync(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`);

if (process.env.GITHUB_OUTPUT) {
  writeFileSync(process.env.GITHUB_OUTPUT, `schema_path=${schemaPath}\nmanifest_path=${manifestPath}\n`, { flag: "a" });
}

console.log(JSON.stringify({ schemaPath, manifestPath, target, engine, schemaName, schemaLabel }, null, 2));
